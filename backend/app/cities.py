def resolve_city_code(value: str) -> str:
    cleaned = value.strip()
    if len(cleaned) == 3 and cleaned.isalpha():
        return cleaned.upper()

    code = RUSSIAN_CITY_CODES.get(cleaned.lower())
    if not code:
        raise ValueError(f"Не знаю город '{value}'. Введи IATA-код или добавь город в справочник.")
    return code
